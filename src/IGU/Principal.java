    private void btnAteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAteriorActionPerformed
        // Obtengo el valor de "txtIndice" lo convierto a entero y lo guardo en índice
        indice = Integer.parseInt(txtIndice.getText());
        
        //Compruebo si el índice sea mayor a 0 
        if(indice>0){
            indice--;                   //Decremento el índice
            irA(indice);              //Actualizo los datos en el índice modificado.
        }
    }//GEN-LAST:event_btnAteriorActionPerformed

    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        // Obtengo el valor de "txtIndice" lo convierto a entero y lo guardo en índice
        indice = Integer.parseInt(txtIndice.getText());
        //Compruebo si el índice sea menor al tamaño del vector.
        if(indice+1<tamanio){
            indice++;               //Incremento el índice
            irA(indice);             //Actualizo los datos en el índice modificado.
        }
    }//GEN-LAST:event_btnSiguienteActionPerformed

