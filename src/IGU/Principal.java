    /**
     * Actualiza todos los textField de la vista incluyendo el indice
     * @param indice numero entero entre 0 y tamanio maximo del vector
     */
    private void irA(int indice) {
        txtDNI.setText(dnis[indice]) ;
        txtApellido.setText(apellidos[indice]) ;
        txtNombre.setText(nombres[indice]) ;
        txtDireccion.setText(direcciones[indice]) ;
        txtTel.setText(telefonos[indice]) ;
        txtNac.setText(nacimientos[indice]) ;
        
        txtIndice.setText(String.valueOf(indice));
    }
    /**
     * Método guardar que modifica los valores de los vectores en el índice actual
     */
    private void guardar() {
        indice = Integer.parseInt(txtIndice.getText());
        
        dnis[indice] = txtDNI.getText();
        apellidos[indice] = txtApellido.getText();
        nombres[indice] = txtNombre.getText();
        direcciones[indice] = txtDireccion.getText();
        telefonos[indice] = txtTel.getText();
        nacimientos[indice] = txtNac.getText();
    }
